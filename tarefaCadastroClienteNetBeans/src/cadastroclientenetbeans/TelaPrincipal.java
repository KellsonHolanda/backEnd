    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nome = textAreaNome.getText();
        var cpf = textAreaCPF.getText();
        String ender = textAreaEnder.getText();
        String num = textAreaNum.getText();
        String tel = textAreaTel.getText();
        String cidade = textAreaCidade.getText();
        String estado = textAreaEstado.getText();
        
        if(!camposValidos(nome, cpf, ender, num, tel, cidade, estado)){
                    JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios. Ainda existem campos não cadastrados", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    return;

        }
        
        Cliente cliente = new Cliente(nome,cpf, tel, ender, num, cidade, estado);
        Boolean semCadastrado = this.clienteDAO.cadastrar(cliente);
        
        if (semCadastrado) {
            modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getEnder(),cliente.getNumero(), cliente.getTel(), cliente.getCidade(), cliente.getEstado()});
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Cliente já se encontra cadastrado", "ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaCliente.getSelectedRow();
       
       if (linhaSelecionada >= 0) {
            int result = JOptionPane.showConfirmDialog(this,"Deseja realmente excluir este cliente?", "CUIDADO",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
        
            if(result == JOptionPane.YES_OPTION){

                Long cpf = (Long)tabelaCliente.getValueAt(linhaSelecionada, 1);
                this.clienteDAO.excluir(cpf);
                modelo.removeRow(linhaSelecionada);

                JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado.", "ERRO",JOptionPane.INFORMATION_MESSAGE);
        }
       
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tabelaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClienteMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tabelaCliente.getSelectedRow();
        Long cpf = (Long) tabelaCliente.getValueAt(linhaSelecionada, 1);

        Cliente cliente  = this.clienteDAO.consultar(cpf);

        textAreaNome.setText(cliente.getNome());
        textAreaCPF.setText(cliente.getCpf().toString());
        textAreaEnder.setText(cliente.getEnder());
        textAreaNum.setText(cliente.getNumero().toString());
        textAreaTel.setText(cliente.getTel().toString());
        textAreaCidade.setText(cliente.getCidade());
        textAreaEstado.setText(cliente.getEstado());
    }//GEN-LAST:event_tabelaClienteMouseClicked

