package tarefa_media;

public class CalcularMedia {
	
	public static void main(String args[]) {
		
		int nota1 = 10;
		int nota2 = 7;
		double nota3 = 8.50;
		int nota4 = 6;
		
		double media = (nota1 + nota2 + nota3 + nota4)/4;
				
		System.out.println("A media das notas dos quatro alunos e': " + media);
	}

}
